class Downsample(nn.Module):
    """
    A downsampling module that applies a convolution operation to reduce the spatial dimensions of the input.

    Args:
        in_channel (int): Number of input channels.
        out_channel (int): Number of output channels.
    """
    def __init__(self, in_channel: int, out_channel: int):
        super(Downsample, self).__init__()
        self.conv = nn.Sequential(
            nn.Conv2d(in_channel, out_channel, kernel_size=4, stride=2, padding=1),
        )
        self.in_channel = in_channel
        self.out_channel = out_channel

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        """
        Forward pass of the downsampling module.

        Args:
            x (torch.Tensor): Input tensor with shape (B, L, C), where B is the batch size,
                              L is the sequence length, and C is the number of channels.

        Returns:
            torch.Tensor: Output tensor with reduced spatial dimensions.
        """
        B, L, C = x.shape
        H = int(math.sqrt(L))
        W = int(math.sqrt(L))

        if H * W != L:
            raise ValueError(f"The length L must be a perfect square, but got L={L} which does not correspond to H*W={H}*{W}.")

        x = x.transpose(1, 2).contiguous().view(B, C, H, W)
        out = self.conv(x).flatten(2).transpose(1, 2).contiguous()  # B H*W C
        return out

    
class Upsample(nn.Module):
    """
    An upsampling module that applies a deconvolution operation to increase the spatial dimensions of the input.

    Args:
        in_channel (int): Number of input channels.
        out_channel (int): Number of output channels.
    """
    def __init__(self, in_channel: int, out_channel: int):
        super(Upsample, self).__init__()
        self.deconv = nn.Sequential(
            nn.ConvTranspose2d(in_channel, out_channel, kernel_size=2, stride=2),
        )
        self.in_channel = in_channel
        self.out_channel = out_channel

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        """
        Forward pass of the upsampling module.

        Args:
            x (torch.Tensor): Input tensor with shape (B, L, C), where B is the batch size,
                              L is the sequence length, and C is the number of channels.

        Returns:
            torch.Tensor: Output tensor with increased spatial dimensions.
        """
        B, L, C = x.shape
        H = int(math.sqrt(L))
        W = int(math.sqrt(L))

        if H * W != L:
            raise ValueError(f"The length L must be a perfect square, but got L={L} which does not correspond to H*W={H}*{W}.")

        x = x.transpose(1, 2).contiguous().view(B, C, H, W)
        out = self.deconv(x).flatten(2).transpose(1, 2).contiguous()  # B H*W C
        return out
    
class InputProj(nn.Module):
    """
    An input projection module that applies a convolution and an optional normalization to the input.

    Args:
        in_channel (int): Number of input channels. Defaults to 3.
        out_channel (int): Number of output channels. Defaults to 64.
        kernel_size (int): Size of the convolving kernel. Defaults to 3.
        stride (int): Stride of the convolution. Defaults to 1.
        norm_layer (nn.Module, optional): Normalization layer to be applied after the convolution. Defaults to None.
        act_layer (nn.Module): Activation layer to be applied after the convolution. Defaults to nn.LeakyReLU.
    """
    def __init__(
        self,
        in_channel: int = 3,
        out_channel: int = 64,
        kernel_size: int = 3,
        stride: int = 1,
        norm_layer: nn.Module = None,
        act_layer: nn.Module = nn.LeakyReLU
    ):
        super().__init__()
        self.proj = nn.Sequential(
            nn.Conv2d(in_channel, out_channel, kernel_size=kernel_size, stride=stride),
            act_layer(inplace=True)
        )
        self.norm = norm_layer(out_channel) if norm_layer is not None else None
        self.in_channel = in_channel
        self.out_channel = out_channel

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        """
        Forward pass of the input projection module.

        Args:
            x (torch.Tensor): Input tensor with shape (B, C, H, W), where B is the batch size,
                              C is the number of channels, H and W are the height and width of the input.

        Returns:
            torch.Tensor: Output tensor with shape (B, H*W, C) after convolution and optional normalization.
        """
        B, C, H, W = x.shape
        x = self.proj(x).flatten(2).transpose(1, 2).contiguous()  # B H*W C
        if self.norm is not None:
            x = self.norm(x)
        return x


class OutputProj(nn.Module):
    """
    An output projection module that applies a convolution and an optional normalization and activation to the input.

    Args:
        in_channel (int): Number of input channels. Defaults to 64.
        out_channel (int): Number of output channels. Defaults to 3.
        kernel_size (int): Size of the convolving kernel. Defaults to 3.
        stride (int): Stride of the convolution. Defaults to 1.
        norm_layer (nn.Module, optional): Normalization layer to be applied after the convolution. Defaults to None.
        act_layer (nn.Module, optional): Activation layer to be applied after the convolution. Defaults to None.
    """
    def __init__(
        self,
        in_channel: int = 64,
        out_channel: int = 3,
        kernel_size: int = 3,
        stride: int = 1,
        norm_layer: nn.Module = None,
        act_layer: nn.Module = None
    ):
        super().__init__()
        self.proj = nn.Sequential(
            nn.Conv2d(in_channel, out_channel, kernel_size=kernel_size, stride=stride, padding=kernel_size // 2),
        )
        if act_layer is not None:
            self.proj.add_module("activation", act_layer(inplace=True))
        self.norm = norm_layer(out_channel) if norm_layer is not None else None
        self.in_channel = in_channel
        self.out_channel = out_channel

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        """
        Forward pass of the output projection module.

        Args:
            x (torch.Tensor): Input tensor with shape (B, L, C), where B is the batch size,
                              L is the sequence length, and C is the number of channels.

        Returns:
            torch.Tensor: Output tensor after convolution and optional normalization and activation.
        """
        B, L, C = x.shape
        H = int(math.sqrt(L))
        W = int(math.sqrt(L))

        if H * W != L:
            raise ValueError(f"The length L must be a perfect square, but got L={L} which does not correspond to H*W={H}*{W}.")

        x = x.transpose(1, 2).view(B, C, H, W)
        x = self.proj(x)
        if self.norm is not None:
            x = self.norm(x)
        return x

